package com.yr.io;


public class IODTO {
	private int num;
	private int pnum;
	private int in_pct;
	private String in_date;
	private int out_pct;
	private String out_date;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getPnum() {
		return pnum;
	}
	public void setPnum(int pnum) {
		this.pnum = pnum;
	}
	public int getIn_pct() {
		return in_pct;
	}
	public void setIn_pct(int in_pct) {
		this.in_pct = in_pct;
	}
	public String getIn_date() {
		return in_date;
	}
	public void setIn_date(String in_date) {
		this.in_date = in_date;
	}
	public int getOut_pct() {
		return out_pct;
	}
	public void setOut_pct(int out_pct) {
		this.out_pct = out_pct;
	}
	public String getOut_date() {
		return out_date;
	}
	public void setOut_date(String out_date) {
		this.out_date = out_date;
	}
	
	
}
